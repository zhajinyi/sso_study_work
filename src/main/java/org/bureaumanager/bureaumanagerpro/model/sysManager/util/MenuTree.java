package org.bureaumanager.bureaumanagerpro.model.sysManager.util;

public class MenuTree {
    private String title;
    private String value;
    private boolean checked = false;//是否选中，false为不选中
    private boolean disabled = false;//是否可用，false为可用
    private MenuTree[] data;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public boolean isChecked() {
        return checked;
    }

    public void setChecked(boolean checked) {
        this.checked = checked;
    }

    public boolean isDisabled() {
        return disabled;
    }

    public void setDisabled(boolean disabled) {
        this.disabled = disabled;
    }

    public MenuTree[] getData() {
        return data;
    }

    public void setData(MenuTree[] data) {
        this.data = data;
    }
}
